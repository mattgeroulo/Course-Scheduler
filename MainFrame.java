    private void ScheduleCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleCourseButtonActionPerformed
        StudentAddedLabel.setText(StudentComboBox.getSelectedItem()+ " has been scheduled for this class");
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        String courseCode = CourseComboBox.getSelectedItem().toString();
        String temp = StudentComboBox.getSelectedItem().toString();
        
        String[] temp2 = temp.split("[,]",0);             
        String firstName = temp2[0];
        String LastName = temp2[1];
        // needs to get seats possible, which is seats for course
        //reserved seats is gonna be the count function
        
        int seatsPossible = CourseQueries.getCourseSeats(currentSemester,courseCode);
        int reservedSeats = ScheduleEntryQueries.getScheduledStudentCount(currentSemester, courseCode);
        String studentid = StudentQueries.getStudentID(firstName,LastName); 
        if (reservedSeats< seatsPossible)
        {
            ScheduleEntryQueries.addScheduleEntry( currentSemester, courseCode,  studentid, "s", currentTimestamp );
            scheduleCourseStatusLabel.setText(StudentComboBox.getSelectedItem().toString() + " has been scheduled for this class");
        }
        else
        {
            ScheduleEntryQueries.addScheduleEntry( currentSemester, courseCode,  studentid, "w", currentTimestamp );
             scheduleCourseStatusLabel.setText(StudentComboBox.getSelectedItem().toString() + " has been waitlisted for this class");

        }
    }//GEN-LAST:event_ScheduleCourseButtonActionPerformed

