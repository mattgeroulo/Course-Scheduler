    public void rebuildDropStudentBox()
    {
        if (ChangeStudentDropBox.getItemCount()!=0)
        {

        String temp = ChangeStudentDropBox.getSelectedItem().toString();
        String temp_[] = temp.split("[,]");
        String first = temp_[0];
        String Last = temp_[1];
        String Student = StudentQueries.getStudentID(first, Last);
        ArrayList<String> courses = ScheduleEntryQueries.getCourses(currentSemester, Student);
        DropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        }   
        else{
            assert true;
        }
    }
    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes(){

        ArrayList<CourseEntry> course = CourseQueries.getAllCourses(currentSemester);
        ArrayList<Object> course_code = new ArrayList<Object>();

        if (course.size()>0)
        {
            for (int i =0; i< course.size(); i++)
            {
                CourseEntry value = course.get(i);
                course_code.add(value.getCourseCode());
            }
        }
        else{
            assert true;   
            
            }
       
            CourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(course_code.toArray()));  
            AdminCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(course_code.toArray()));
            AdminDisplayCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(course_code.toArray()));
            DropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(course_code.toArray()));
    }
    
    public void rebuildStudentComboBox(){
        ArrayList<StudentEntry> Student = StudentQueries.getAllStudents();
        ArrayList<Object> Name = new ArrayList<Object>();
        Name.clear();

        
        for (int i = 0; i< Student.size(); i++)
        {
            
            StudentEntry student = Student.get(i);
            Name.add(student.getLastName() +","+ student.getFirstName());
        }        
        StudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(Name.toArray()));
        DisplayScheduleStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(Name.toArray()));
        ChangeStudentDropBox.setModel(new javax.swing.DefaultComboBoxModel(Name.toArray()));
        
    }
